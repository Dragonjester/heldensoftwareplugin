package logic;

import java.io.Serializable;

public class eigenschaft implements Serializable{
	public String wert = "";
	public String name= "";
	public String mod = "";

	eigenschaft(String wert, String name){
		if(wert.charAt(0) == ' ')
			this.wert = wert.substring(1);
		else
			this.wert = wert;
		this.name = name;
	}
}